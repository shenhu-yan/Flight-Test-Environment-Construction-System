async def generate_env_package(
    env: Env,
    config: EnvConfig,
    db: AsyncSession,
) -> str:
    """Generate a downloadable environment package and upload to MinIO."""
    try:
        from minio import Minio
        from app.config import settings

        minio_client = Minio(
            settings.MINIO_ENDPOINT,
            access_key=settings.MINIO_ACCESS_KEY,
            secret_key=settings.MINIO_SECRET_KEY,
            secure=settings.MINIO_SECURE,
        )
    except Exception as e:
        logger.error(f"Failed to initialize MinIO client: {e}")
        raise

    if not minio_client.bucket_exists(settings.BUCKET_NAME):
        minio_client.make_bucket(settings.BUCKET_NAME)

    buffer = io.BytesIO()
    with zipfile.ZipFile(buffer, "w", zipfile.ZIP_DEFLATED) as zf:
        config_dict = config.model_dump()
        zf.writestr("config.json", json.dumps(config_dict, indent=2))

        zf.writestr("core.py", CORE_PY_TEMPLATE)
        zf.writestr("reward.py", REWARD_PY_TEMPLATE)

        scene_json = json.dumps({
            "terrain": config_dict.get("terrain", {}),
            "weather": config_dict.get("weather", {}),
            "obstacles": config_dict.get("obstacles", {}),
            "waypoints": config_dict.get("waypoints", []),
            "runway": {
                "length": 2000,
                "width": 30,
                "heading": 0,
                "position": [0, 0, 0],
            },
        }, indent=2)
        zf.writestr("scene.json", scene_json)

        jsbsim_xmls = _generate_jsbsim_xmls(config)
        for name, content in jsbsim_xmls.items():
            zf.writestr(f"jsbsim/{name}", content)

    buffer.seek(0)
    object_name = f"envs/{env.id}/package.zip"
    minio_client.put_object(
        settings.BUCKET_NAME,
        object_name,
        buffer,
        length=buffer.getbuffer().nbytes,
        content_type="application/zip",
    )

    storage_path = f"s3://{settings.BUCKET_NAME}/{object_name}"
    env.storage_path = storage_path
    env.status = "ready"
    await db.commit()

    logger.info(f"Environment package generated for env {env.id}: {storage_path}")
    return storage_path


def _generate_jsbsim_xmls(config: EnvConfig) -> dict[str, str]:
    terrain = config.terrain
    weather = config.weather
    fd = config.flight_dynamics

    ground_xml = f"""<?xml version="1.0" encoding="UTF-8"?>
<ground>
  <terrain type="{terrain.type}">
    <elevation-min>{terrain.elevation_min}</elevation-min>
    <elevation-max>{terrain.elevation_max}</elevation-max>
    <resolution>{terrain.resolution}</resolution>
  </terrain>
</ground>"""

    weather_xml = f"""<?xml version="1.0" encoding="UTF-8"?>
<weather>
  <wind>
    <wind-speed>{weather.wind_speed}</wind-speed>
    <wind-direction-deg>{weather.wind_direction}</wind-direction-deg>
  </wind>
  <visibility>{weather.visibility}</visibility>
</weather>"""

    aircraft_xml = f"""<?xml version="1.0" encoding="UTF-8"?>
<aircraft name="{fd.aircraft_model}">
  <mass>{fd.mass}</mass>
  <wingspan>{fd.wingspan}</wingspan>
</aircraft>"""

    return {
        "ground.xml": ground_xml,
        "weather.xml": weather_xml,
        "aircraft.xml": aircraft_xml,
    }


