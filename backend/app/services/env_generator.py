# Parameters that can be perturbed and their reasonable ranges
PERTURBATION_SPECS = {
    "wind_speed": {"min": 0, "max": 40, "type": "float"},
    "wind_direction": {"min": 0, "max": 360, "type": "float"},
    "visibility": {"min": 500, "max": 20000, "type": "float"},
    "elevation_min": {"min": 0, "max": 2000, "type": "float"},
    "elevation_max": {"min": 500, "max": 5000, "type": "float"},
    "obstacle_count": {"min": 0, "max": 50, "type": "int"},
    "obstacle_density": {"min": 0.0, "max": 1.0, "type": "float"},
    "mass": {"min": 500, "max": 50000, "type": "float"},
    "wingspan": {"min": 5, "max": 80, "type": "float"},
}


def _apply_perturbation(base_config: dict, param_name: str, delta: float) -> dict:
    """
    Apply a single perturbation to a config parameter.
    
    Args:
        base_config: The base configuration dict (will be deep-copied)
        param_name: Dot-notation path like "weather.wind_speed"
        delta: Amount to perturb (positive = increase, negative = decrease)
    
    Returns:
        New config dict with perturbation applied
    """
    config = copy.deepcopy(base_config)
    parts = param_name.split(".")
    
    target = config
    for part in parts[:-1]:
        if part in target and isinstance(target[part], dict):
            target = target[part]
        else:
            # Path doesn't exist, skip
            return config
    
    leaf_key = parts[-1]
    if leaf_key not in target:
        return config
    
    current_val = target[leaf_key]
    spec = PERTURBATION_SPECS.get(param_name, {})
    param_min = spec.get("min", float("-inf"))
    param_max = spec.get("max", float("inf"))
    param_type = spec.get("type", "float")
    
    try:
        new_val = float(current_val) + delta
        new_val = max(param_min, min(param_max, new_val))
        if param_type == "int":
            new_val = int(round(new_val))
        else:
            new_val = round(new_val, 4)
        target[leaf_key] = new_val
    except (ValueError, TypeError):
        logger.warning(f"Cannot perturb {param_name}: non-numeric value {current_val}")
    
    return config


def _generate_batch_configs(
    base_config: dict,
    count: int,
    deltas: Optional[list[dict]] = None,
) -> list[dict]:
    """
    Generate multiple configs by applying perturbations to a base config.
    
    Args:
        base_config: The starting configuration
        count: Number of configs to generate
        deltas: Optional list of {"param": "path", "delta": value} dicts.
                If None, random perturbations are generated.
    
    Returns:
        List of config dicts
    """
    configs = []
    
    if deltas is not None and len(deltas) > 0:
        # Use provided deltas with some randomization
        for i in range(count):
            config = copy.deepcopy(base_config)
            for delta_spec in deltas:
                param = delta_spec.get("param", "")
                delta_val = delta_spec.get("delta", 0)
                # Add some randomness to the delta
                noise = random.uniform(-abs(delta_val) * 0.2, abs(delta_val) * 0.2)
                config = _apply_perturbation(config, param, delta_val + noise)
            configs.append(config)
    else:
        # Generate fully random perturbations
        available_params = list(PERTURBATION_SPECS.keys())
        for i in range(count):
            config = copy.deepcopy(base_config)
            # Apply 2-4 random perturbations per config
            num_perturbations = random.randint(2, min(4, len(available_params)))
            chosen_params = random.sample(available_params, num_perturbations)
            
            for param_name in chosen_params:
                spec = PERTURBATION_SPECS[param_name]
                param_range = spec["max"] - spec["min"]
                # Perturb by up to 30% of the parameter range
                delta = random.uniform(-param_range * 0.3, param_range * 0.3)
                config = _apply_perturbation(config, param_name, delta)
            
            configs.append(config)
    
    return configs


async def batch_generate_envs(
    base_config: dict,
    count: int,
    deltas: Optional[list[dict]] = None,
    project_id: Optional[str] = None,
    creator_id: Optional[str] = None,
    db: AsyncSession = None,
    name_prefix: str = "Batch Env",
) -> list[str]:
    """
    Batch-generate multiple environments with perturbations.
    
    Args:
        base_config: Base configuration to perturb
        count: Number of environments to generate
        deltas: Optional explicit perturbation specs
        project_id: Project to assign envs to
        creator_id: User who created the batch
        db: Database session
        name_prefix: Prefix for environment names
    
    Returns:
        List of created environment IDs
    """
    if db is None:
        raise ValueError("Database session is required")
    
    configs = _generate_batch_configs(base_config, count, deltas)
    env_ids = []
    
    for i, config in enumerate(configs):
        env = Env(
            id=str(uuid.uuid4()),
            project_id=project_id,
            name=f"{name_prefix} #{i + 1}",
            config=config,
            status="draft",
            created_by=creator_id,
        )
        db.add(env)
        env_ids.append(env.id)
    
    await db.commit()
    
    logger.info(
        f"Batch generated {len(env_ids)} environments for project {project_id}"
    )
    
    return env_ids
